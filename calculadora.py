#Crear una calculadora con las operaciones matemáticas básicas utilizando Python

valor1 = 7
valor2 = 4

resultadoSuma = valor1 + valor2

resultadoResta = valor1 - valor2

resultadoMultiplicacion = valor1 * valor2

resultadoDivision = valor1 / valor2

#Método simple
print(resultadoSuma)
print(resultadoResta)
print(resultadoMultiplicacion)
print(resultadoDivision)

#Método por formato
print(f'La suma es: {resultadoSuma}, la resta es: {resultadoResta}, la multiplicacion es: {resultadoMultiplicacion}, la Division es: {resultadoDivision}')


#Método Tradicional
print("La suma es:" ,resultadoSuma, "la resta es: " ,resultadoResta," la multiplicacion es: ",resultadoMultiplicacion, "la Division es: ",resultadoDivision)